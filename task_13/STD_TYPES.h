#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char        u_8;
typedef unsigned short       u_16;
typedef unsigned int         u_32;
typedef unsigned long long   u_64;

typedef signed char          s_8;
typedef signed short         s_16;
typedef signed int           s_32;
typedef signed long long     s_64;

typedef volatile u_8         v_u_8;
typedef volatile u_16        v_u_16;
typedef volatile u_32        v_u_32;
typedef volatile u_64        v_u_64;

#endif /* STD_TYPES_H_ */
